public class ComparisonPractice {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 15;
        if (num1 * num2 > 20) {
            System.out.println("Big");
        } else if (num1 * num2 == 20) {
            System.out.println("Equal");
        } else {
            System.out.println("Small");
        }
    }
}