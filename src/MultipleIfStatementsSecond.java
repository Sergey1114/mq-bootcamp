public class MultipleIfStatementsSecond {
    public static void main(String[] args) {
        int num = 27;
        if (num < 0) {
            System.out.println("Negative");
        } else if (num == 0) {
            System.out.println("Zero");
        } else if (num > 0) {
            System.out.println("Positive");
        }
    }
}
